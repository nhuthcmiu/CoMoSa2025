<p>CSoNet 2025 provides a premier interdisciplinary forum to bring together researchers and practitioners from all fields of big data networks, such as billion-scale network computing, data network analysis, mining, security and privacy, and deep learning. CSoNet 2025 seeks to address emerging yet important computational problems, with a focus on the fundamental background, theoretical technology development, and real-world applications associated with big data network analysis, modeling, and deep learning. The conference solicits theoretical, methodological, empirical, and experimental research reporting original and unpublished results on computational big data networks. The conference will be organized at <a target="_blank" rel="noopener noreferrer" href="https://vku.udn.vn/">Vietnam-Korea University of Information and Communication Technology</a>, Danang, Vietnam.</p>
<p>Topics of interest include, but are not limited to:</p>
<ul style="list-style-type:circle;">
    <li>Real-world Complex Networks Analysis</li>
    <li>Representation Learning on Networks</li>
    <li>Big Data Analysis</li>
    <li>Mathematical Modeling and Analysis of Real-world Networks</li>
    <li>Network Structure Analysis and Dynamics Optimization</li>
    <li>Data Network Design and Architecture</li>
    <li>Information Diffusion Models and Techniques</li>
    <li>Security and Privacy in Data Networks</li>
    <li>Efficient Algorithms for Large-scale Data Networks Computing</li>
    <li>Reputation and Trust in Social Media</li>
    <li>Energy Efficiency in Mobile Data Networks</li>
    <li>Natural Language Understanding for Network Analysis</li>
    <li>E-commerce and Social Media Marketing</li>
    <li>Deep Learning on Graphs and its Application</li>
    <li>Stock Market Prediction and Stock Recommendation</li>
    <li>Multidimensional graph analysis</li>
    <li>Algorithmic fairness in network analysis and graph mining</li>
</ul>
<p>Accepted papers will be published in Springerâ€™s Lecture Notes in Computer Science, and indexed by ISI (CPCI-S, included in ISI Web of Science), EI Engineering Index (Compendex and Inspec databases), ACM Digital Library, DBLP, Google Scholar, MathSciNet, etc. CSoNet will award prizes for the best paper and best student paper as judged by a special award committee. Also, extended versions of selected best papers will be invited for publication in Journal of Combinatorial Optimization and Applied Network Science.</p>
<p>Authors who are interested in the above topics can submit their unpublished work to&nbsp;CSoNet&nbsp;2025. A clear indication of the motivation and comparison with prior related work should be presented. Simultaneous submission to a journal or another conference with refereed proceedings is not allowed.</p>
